ROW = 100
COL = 100

block_count =[]

def displaylist(lst):
    print("c(", end='')
    for i in range(len(lst)):
        if i == len(lst) - 1:
            print("%i)" % lst[i])
        else:
            print("%i," % lst[i], end='')

class MainWindow(pyglet.window.Window):
    def __init__(self,*args,**Kwargs):

        self.RunState = True
        super().__init__(*args,**Kwargs)
        pyglet.clock.schedule_interval(self.update, 0.2)
        self.size = floor(args[0]/ROW)
        #####
        #self.LifeGame = LifeGame(ROW,COL,alive=1,infect_rate=0.1,sick_percent=0.02,self_healing_rate=0.01)
        self.LifeGame = LifeGame(ROW,COL,alive=0.2)
        self.times = 0

    def on_draw(self):
        self.clear()
        pyglet.gl.glClearColor(1, 1, 1, 1)
        self.show()

    def on_key_press(self, symbol, modifiers):
        displaylist(block_count)
        if(self.RunState):
            pyglet.clock.unschedule(self.update)
            self.RunState = False;
        else:
            pyglet.clock.schedule_interval(self.update, 0.2)
            self.RunState = True;

    def show(self):
        display = self.LifeGame.display()
        block_count.append(0)
        for i in range(0, self.LifeGame.rows):
            for j in range(0, self.LifeGame.cols):
                color = (0, 0, 0, 255)
                if display[i][j] == 1:
                    color = (225, 225, 225,225)
                    block_count[self.times] = block_count[self.times] +1
                elif display[i][j] == 2:
                    color = (213, 89, 89,225)
                elif display[i][j] == 3:
                    color = (115, 198, 182, 225)

                self.rect((i * self.size, j * self.size), self.size, self.size, color)

    def rect(self, pos, width, height, color):
        pyglet.graphics.draw(4, pyglet.gl.GL_TRIANGLE_STRIP,
                             ("v2f", (pos[0], pos[1], pos[0], pos[1] + height, pos[0] + width, pos[1], pos[0] + width,
                                      pos[1] + height)),
                             ("c4B", tuple(color[i] for i in range(0, 4)) * 4))

    def update(self, dt):
        self.times = self.times +1
        print("itera : %i" % self.times)
        if(self.times!=1):
            self.LifeGame.move()
        print("FPS : % f" % pyglet.clock.get_fps())


window = MainWindow(800, 800,"Henrys Cellular automaton")
pyglet.app.run()
